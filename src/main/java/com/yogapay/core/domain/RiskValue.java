package com.yogapay.core.domain;

public class RiskValue extends ToString{
	private Long id;
	private Long eachMax;	//单笔最大金额
	private Long eachMin;	//单笔最小金额
	private Long creditCardEachMax;	//信用卡单笔最大金额
	private Long creditCardTerminalDailyMax;	//单终端信用卡单日累计限额
	private Long terminalDailyMax;	//单终端单日累计限额
	private Long creditCardTerminalMonthMax;	//单终端信用卡当月累计限额
	private Long terminalMonthMax;	//单终端当月累计限额
	private Long sameCardCount;	//同卡交易笔数限制
	private Long count;	//交易笔数限制
	private String limitTimeBegin;	//交易起始时间限制
	private String limitTimeEnd; //交易结束时间限制
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getEachMax() {
		return eachMax;
	}
	public void setEachMax(Long eachMax) {
		this.eachMax = eachMax;
	}
	public Long getEachMin() {
		return eachMin;
	}
	public void setEachMin(Long eachMin) {
		this.eachMin = eachMin;
	}
	public Long getCreditCardEachMax() {
		return creditCardEachMax;
	}
	public void setCreditCardEachMax(Long creditCardEachMax) {
		this.creditCardEachMax = creditCardEachMax;
	}
	public Long getCreditCardTerminalDailyMax() {
		return creditCardTerminalDailyMax;
	}
	public void setCreditCardTerminalDailyMax(Long creditCardTerminalDailyMax) {
		this.creditCardTerminalDailyMax = creditCardTerminalDailyMax;
	}
	public Long getTerminalDailyMax() {
		return terminalDailyMax;
	}
	public void setTerminalDailyMax(Long terminalDailyMax) {
		this.terminalDailyMax = terminalDailyMax;
	}
	public Long getCreditCardTerminalMonthMax() {
		return creditCardTerminalMonthMax;
	}
	public void setCreditCardTerminalMonthMax(Long creditCardTerminalMonthMax) {
		this.creditCardTerminalMonthMax = creditCardTerminalMonthMax;
	}
	public Long getTerminalMonthMax() {
		return terminalMonthMax;
	}
	public void setTerminalMonthMax(Long terminalMonthMax) {
		this.terminalMonthMax = terminalMonthMax;
	}
	public Long getSameCardCount() {
		return sameCardCount;
	}
	public void setSameCardCount(Long sameCardCount) {
		this.sameCardCount = sameCardCount;
	}
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	public String getLimitTimeBegin() {
		return limitTimeBegin;
	}
	public void setLimitTimeBegin(String limitTimeBegin) {
		this.limitTimeBegin = limitTimeBegin;
	}
	public String getLimitTimeEnd() {
		return limitTimeEnd;
	}
	public void setLimitTimeEnd(String limitTimeEnd) {
		this.limitTimeEnd = limitTimeEnd;
	}
}
