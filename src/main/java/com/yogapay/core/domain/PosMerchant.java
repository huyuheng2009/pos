package com.yogapay.core.domain;

//商户信息
public class PosMerchant extends ToString {

	private Long id;
	private String merchantNo;
	private String permission;
	private String merchantName;
	private String shortName;
	private String type;
    private String bizPhone;
    private String merchantType;
    private String payType;

	public String getMerchantName() {
		return merchantName;
	}

	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}

	private Integer settleDays;

	public Integer getSettleDays() {
		return settleDays;
	}

	public void setSettleDays(Integer settleDays) {
		this.settleDays = settleDays;
	}

	public String getPermission() {
		return permission;
	}

	public void setPermission(String permission) {
		this.permission = permission;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getMerchantNo() {
		return merchantNo;
	}

	public void setMerchantNo(String merchantNo) {
		this.merchantNo = merchantNo;
	}

	public String getShortName() {
		return shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

    public String getBizPhone() {
        return bizPhone;
    }

    public void setBizPhone(String bizPhone) {
        this.bizPhone = bizPhone;
    }

    public String getMerchantType() {
        return merchantType;
    }

    public void setMerchantType(String merchantType) {
        this.merchantType = merchantType;
    }

    public String getPayType() {
        return payType;
    }

    public void setPayType(String payType) {
        this.payType = payType;
    }
}
