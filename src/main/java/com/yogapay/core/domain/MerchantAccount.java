package com.yogapay.core.domain;

import java.math.BigDecimal;

/**
 * @Todo: 账户
 * @Author: Zhanggc
 */
public class MerchantAccount {
    private Integer id;
    private String merchantNo;
    private BigDecimal amount;
    private BigDecimal freezeAmount;
    private BigDecimal avaliableAmount;
    private String status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getMerchantNo() {
        return merchantNo;
    }

    public void setMerchantNo(String merchantNo) {
        this.merchantNo = merchantNo;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public BigDecimal getFreezeAmount() {
        return freezeAmount;
    }

    public void setFreezeAmount(BigDecimal freezeAmount) {
        this.freezeAmount = freezeAmount;
    }

    public BigDecimal getAvaliableAmount() {
        return avaliableAmount;
    }

    public void setAvaliableAmount(BigDecimal avaliableAmount) {
        this.avaliableAmount = avaliableAmount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
